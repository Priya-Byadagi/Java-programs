class Arithmetic3Operators{
public static void main(String args[]){
	int n1=10,n2=5,n3=1;
	int sum, sub,mult,div,mod;
	sum=n1+n2+n3;
	sub=n1-n2-n3;
	mult=n1*n2*n3;
	div=n1/n2/n3;
	mod=n1%n2%n3;
	System.out.println("sum is:"+sum+'\n'+"sub is:"+sub+'\n'+"mult is:"+sub+'\n'+"div is:"+div+'\n'+"mod is:"+mod);





}
}